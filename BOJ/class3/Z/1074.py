import sys

N, r, c = map(int, sys.stdin.readline().rstrip().split(' '))
n = pow(2, N)
order = 0
if r < n or c < n:

	while n > 0:
		n = n//2
		if r+1 <= n and c+1 > n:
			order += n*n
			c = c - n
		elif r+1 > n and c+1 <= n:
			order += n*n*2
			r = r - n
